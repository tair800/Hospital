// Helper functions for data manipulation
export const getEventById = (id) => {
    return eventData.find(event => event.id === id);
};

export const getEventsByCategory = (category) => {
    return eventData.filter(event => event.category === category);
};

export const getUpcomingEvents = () => {
    const now = new Date();
    return eventData.filter(event => new Date(event.eventDate) > now);
};

export const getFeaturedEvents = () => {
    return eventData.filter(event => event.isFeatured);
};

export const getEventsByDateRange = (startDate, endDate) => {
    const start = new Date(startDate);
    const end = new Date(endDate);
    return eventData.filter(event => {
        const eventDate = new Date(event.eventDate);
        return eventDate >= start && eventDate <= end;
    });
};
